package com.example.demo.model;

public class LineData {
    public double startX;
    public double endX;
    public double startY;
    public double endY;
    public double angle;

    public LineData(double startX, double endX, double startY, double endY, double angle) {
        this.startX = startX;
        this.endX = endX;
        this.startY = startY;
        this.endY = endY;
        this.angle = angle;
    }
}
